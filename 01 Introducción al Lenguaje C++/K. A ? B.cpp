#include <iostream>

using namespace std;

int main()
{
   int A, B, C;           
   cin >> A >> B;
    // Suma   
   C = A + B;
   cout << C << endl;
   // Resta
   C = A - B;
   cout << C << endl;
   // Multiplicación
   C = A * B;
   cout << C << endl;
   // División
   C = A / B;
   cout << C << endl;
   // Modulo (Residuo de la División entera)
   C = A % B;
   cout << C << endl;
    return 0;
}